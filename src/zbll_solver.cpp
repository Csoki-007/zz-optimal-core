void solve_ZBLL(Cube* cube) {
    ZZ_PROFILE_SCOPE(SolveZBLL);
    std::array<int, 11> lastLayer = {
        cube->cube[0], cube->cube[9], cube->cube[38], cube->cube[2], cube->cube[36], cube->cube[29],
        cube->cube[4], cube->cube[27], cube->cube[20], cube->cube[28], cube->cube[19]
    };
    int target = cube->cube[37];
    const char* result = nullptr;
    zbll_last_lookup_failed = false;
    zbll_last_case[0] = '\0';
    strncpy(zbll_last_family, "None", sizeof(zbll_last_family) - 1);
    zbll_last_family[sizeof(zbll_last_family) - 1] = '\0';

    // Le redukĂˇljuk a lehetsĂ©ges szĂ­neket 4 opciĂłra tulajdonsĂˇguk alapjĂˇn: correct, none, opposite, adjacent
    static const uint8_t opposite_of[7] = {0, 6, 4, 5, 2, 3, 1};
    const int none_color = cube->cube[8];
    char ll[12];
    for (int i = 0; i < static_cast<int>(lastLayer.size()); ++i) {
        int val = lastLayer[i];

        if (target == val) {
            ll[i] = '3';
        }
        else if (val == none_color) {
            ll[i] = '4';
        }
        else if (val == opposite_of[target]) {
            ll[i] = '2';
        }
        else {
            ll[i] = '1';
        }
    }
    ll[11] = '\0';


            //AS
        if (check4(cube->cube[0], cube->cube[18], cube->cube[27], cube->cube[36]) ||
            check4(cube->cube[2], cube->cube[9], cube->cube[18], cube->cube[27]) ||
            check4(cube->cube[4], cube->cube[9], cube->cube[18], cube->cube[36]) ||
            check4(cube->cube[6], cube->cube[9], cube->cube[27], cube->cube[36])) {
            strncpy(zbll_last_family, "AS", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, AS_Table);

            //H
}  else if (check4(cube->cube[9], cube->cube[11], cube->cube[27], cube->cube[29]) ||
            check4(cube->cube[18], cube->cube[20], cube->cube[36], cube->cube[38]) ||
            check4(cube->cube[9], cube->cube[11], cube->cube[27], cube->cube[29]) ||
            check4(cube->cube[18], cube->cube[20], cube->cube[36], cube->cube[38])) {
            strncpy(zbll_last_family, "H", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, H_Table);

            //L
}  else if (check4(cube->cube[2], cube->cube[6], cube->cube[27], cube->cube[38]) ||
            check4(cube->cube[0], cube->cube[4], cube->cube[18], cube->cube[29]) ||
            check4(cube->cube[2], cube->cube[6], cube->cube[9], cube->cube[20]) ||
            check4(cube->cube[0], cube->cube[4], cube->cube[11], cube->cube[36])) {
            strncpy(zbll_last_family, "L", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, L_Table);

            //Pi
}  else if (check4(cube->cube[9], cube->cube[11], cube->cube[20], cube->cube[36]) ||
            check4(cube->cube[11], cube->cube[27], cube->cube[36], cube->cube[38]) ||
            check4(cube->cube[18], cube->cube[27], cube->cube[29], cube->cube[38]) ||
            check4(cube->cube[9], cube->cube[18], cube->cube[20], cube->cube[29])) {
            strncpy(zbll_last_family, "Pi", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, Pi_Table);

            //S
}  else if (check4(cube->cube[6], cube->cube[20], cube->cube[29], cube->cube[38]) ||
            check4(cube->cube[0], cube->cube[11], cube->cube[20], cube->cube[29]) ||
            check4(cube->cube[2], cube->cube[11], cube->cube[20], cube->cube[38]) ||
            check4(cube->cube[4], cube->cube[11], cube->cube[29], cube->cube[38])) {
            strncpy(zbll_last_family, "S", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, S_Table);

            //T
}  else if (check4(cube->cube[4], cube->cube[6], cube->cube[9], cube->cube[29]) ||
            check4(cube->cube[0], cube->cube[6], cube->cube[20], cube->cube[36]) ||
            check4(cube->cube[0], cube->cube[2], cube->cube[11], cube->cube[27]) ||
            check4(cube->cube[2], cube->cube[4], cube->cube[18], cube->cube[38])) {
            strncpy(zbll_last_family, "T", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, T_Table);

            //U
}  else if (check4(cube->cube[4], cube->cube[6], cube->cube[36], cube->cube[38]) ||
            check4(cube->cube[0], cube->cube[6], cube->cube[27], cube->cube[29]) ||
            check4(cube->cube[0], cube->cube[2], cube->cube[18], cube->cube[20]) ||
            check4(cube->cube[2], cube->cube[4], cube->cube[9], cube->cube[11])) {
            strncpy(zbll_last_family, "U", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, U_Table);

            //PLL
}  else {
            // Ha a kocka megvan oldva
            if (check3(cube->cube[18], cube->cube[19], cube->cube[20]) &&
                check3(cube->cube[27], cube->cube[28], cube->cube[29])) {
                // AUF - Adjust U Face
                if (cube->cube[19] == cube->cube[17]) {
                    U(cube);
                    return;
                } else if (cube->cube[19] == cube->cube[35]) {
                    Ui(cube);
                    return;
                } else if (cube->cube[19] == cube->cube[44]) {
                    U2(cube);
                    return;
                } else {
                    return;
                }
            }
            
            strncpy(zbll_last_family, "PLL", sizeof(zbll_last_family) - 1);
            result = get_ZBLL_Index(ll, PLL_Table);
            
            
}


    // Result string dekĂłdolĂˇs Ă©s lĂ©pĂ©sek vĂ©grehajtĂˇsa a kockĂˇn
    if (result == nullptr) {
        zbll_last_lookup_failed = true;
        strncpy(zbll_last_case, ll, sizeof(zbll_last_case) - 1);
        zbll_last_case[sizeof(zbll_last_case) - 1] = '\0';
        return;
    }

    for (const char* p = result; *p != '\0'; p++) {
        switch (*p) {
            case 'A': U(cube); break;
            case 'B': Ui(cube); break;
            case 'C': U2(cube); break;
            case 'D': L(cube); break;
            case 'E': Li(cube); break;
            case 'F': L2(cube); break;
            case 'G': F(cube); break;
            case 'H': Fi(cube); break;
            case 'I': F2(cube); break;
            case 'J': R(cube); break;
            case 'K': Ri(cube); break;
            case 'L': R2(cube); break;
            case 'M': B(cube); break;
            case 'N': Bi(cube); break;
            case 'O': B2(cube); break;
            case 'P': D(cube); break;
            case 'Q': Di(cube); break;
            case 'R': D2(cube); break;
        }
    }

    // AUF - Adjust U Face
    if (cube->cube[19] == cube->cube[17]) {
        U(cube);
    } else if (cube->cube[19] == cube->cube[35]) {
        Ui(cube);
    } else if (cube->cube[19] == cube->cube[44]) {
        U2(cube);
    } else {
        return;
    }

}


bool check4(char a, char b, char c, char d) {
    return (a == b) && (b == c) && (c == d);
}
bool check3(char a, char b, char c) {
    return (a == b) && (b == c);
}

template <size_t N> const char* get_ZBLL_Index(const char ll[11], const std::array<std::pair<uint32_t, const char*>, N>& table) noexcept {
    const uint32_t code = packKey(ll);
    // csak a kulcsot nĂ©zzĂĽk, a mĂˇsodik mezĹ‘ tetszĹ‘leges
    std::pair<uint32_t,const char*> target{code, nullptr};

    auto it = std::lower_bound(
        table.begin(), table.end(),
        target,
        [](auto const& a, auto const& b){
            return a.first < b.first;
        }
    );
    if (it != table.end() && it->first == code)
        return it->second;
    return nullptr;
}
